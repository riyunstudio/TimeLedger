interface Props {
  icon: string
  size?: 'xs' | 'sm' | 'md' | 'lg' | 'xl' | '2xl' | '3xl' | '4xl' | '5xl' | 'custom'
  color?: string
  customSize?: string
}

const props = withDefaults(defineProps<Props>(), {
  size: 'md',
  color: 'currentColor',
})

const iconName = computed(() => props.icon)

const sizeClass = computed(() => {
  if (props.size === 'custom' && props.customSize) {
    return ''
  }

  const sizeMap: Record<string, string> = {
    xs: 'w-3 h-3',
    sm: 'w-4 h-4',
    md: 'w-5 h-5',
    lg: 'w-6 h-6',
    xl: 'w-7 h-7',
    '2xl': 'w-8 h-8',
    '3xl': 'w-10 h-10',
    '4xl': 'w-12 h-12',
    '5xl': 'w-16 h-16',
  }

  return sizeMap[props.size] || 'w-5 h-5'
})

const colorClass = computed(() => {
  return props.color !== 'currentColor' ? props.color : ''
})
